public class Hero
{
    private int x;
    private int y;
    public Hero(int xi, int yi)
    {
        x = xi;
        y = yi;
    }
    public int get_x()
    {
        return x;
    }
    public int get_y()
    {
        return y;
    }
    public void set_x(int xi)
    {
        x = xi;
    }
    public void set_y(int yi)
    {
        y = yi;
    }
}
